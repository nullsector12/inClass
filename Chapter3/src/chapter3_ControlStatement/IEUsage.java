package chapter3_ControlStatement;

public class IEUsage {
	public static void main(String[] args) {
		
	int num = -5 ;
	
	if (num >=0 ) { 
		System.out.println("num은 양수 입니다.");
	}else {
		System.out.println("num은 음수 입니다.");
	}
	if((num%2) == 0) {
		System.out.println("num은 짝수 입니다.");
	} else {
		System.out.println("num은 홀수 입니다.");
	}
		
	}
}
